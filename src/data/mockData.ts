// Helper functions
export const getPlayerById = (id: string): Player | undefined => 
  players.find(p => p.id === id);

export const getMatchById = (id: string): Match | undefined => 
  matches.find(m => m.id === id);

export const getPlayersByPosition = (position: Position): Player[] =>
  players.filter(p => p.position === position);

export const getPlayersByTeam = (team: string): Player[] =>
  players.filter(p => p.team === team);

export const getRecentMatches = (count: number = 5): Match[] =>
  [...matches]
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    .slice(0, count);

export const getLiveMatches = (): Match[] =>
  matches.filter(m => m.status === 'live');

export const getTopRatedPlayers = (count: number = 5): Player[] =>
  [...players]
    .sort((a, b) => b.rating - a.rating)
    .slice(0, count);
